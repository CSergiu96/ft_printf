#include "ftprintf.h"

/*****************/
#include <stdio.h>
/*****************/
int	main(void)
{
	/***********************************************************\
	|	   	Print a Character 									|
	\***********************************************************/
	//char *ptr = "Hello world!";
    //char *np = 0;

	printf("\x1b[31m""/********************************************\n");
	printf("|Print characters (%%c)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("%c %c %c %c %c",'a' , 'b', 129, 92, 0);
	printf("\n");
	printf("%c %c %c %c %c",'a' , 'b', 129, 92, 0);
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print a integer										|
	\***********************************************************/
	int mi;
	int ma;

	mi = ((unsigned int) -1 >> 1) + 1;
	mi*= -1;
	ma = (unsigned int) -1 >> 1;

	printf("\x1b[31m""/********************************************\n");
	printf("|Print integers (%%d)\n");
	printf("\\********************************************\n""\x1b[32m");
	printf ("Long int :%ld\n", 6500001232212122224L);
	fflush(stdout);
	ft_printf("%d %d %d %d %d", 1, -1, 0, mi, ma);
	printf("\n");
	printf("%d %d %d %d %d", 1, -1, 0, mi, ma);
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print a float										|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print a float (%%f)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("%f",123.456);
	printf("\n");
	printf("%f",123.456);
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print a string										|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print a string (%%s)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("a""%%%s","\\b    c" "%%d" "e""f"); 
	printf("\n");
	printf("a""%%%s","\\b    c" "%%d" "e""f");
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Escape character 									|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Escape Character (%%%%)\n");
	printf("\\********************************************\n""\x1b[32m");
	printf("%%\"\'");
	printf("\n");
	fflush(stdout);
	ft_printf("%%\"\'");
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print %% and a char after with space between		|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print %% and a char after with space between\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("% a");
	//printf("% ");
	printf("<-----Should not work(flag with no specifier)");
	printf("\x1b[36m""\n#############################################\n\n");

	
	return(0);
}