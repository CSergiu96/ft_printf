#include "ftprintf.h"

/*****************/
#include <stdio.h>
/*****************/
int	main(void)
{

	/***********************************************************\
	|	   	Print a Character 									|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print a character (%%c)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("a");
	printf("\n");
	printf("a");
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print a integer										|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print a integer (%%d)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("%d",10);
	printf("\n");
	printf("%d",10);
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print a float										|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print a float (%%f)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("%f",123.456);
	printf("\n");
	printf("%f",123.456);
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print a string										|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print a string (%%s)\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("%s","abc"); 
	printf("\n");
	printf("%s","abc");
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Escape character 									|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Escape Character (%%%%)\n");
	printf("\\********************************************\n""\x1b[32m");
	printf("%%\"\'");
	printf("\n");
	fflush(stdout);
	ft_printf("%%\"\'");
	printf("\x1b[36m""\n#############################################\n\n");

	/***********************************************************\
	|	   	Print %% and a char after with space between		|
	\***********************************************************/
	printf("\x1b[31m""/********************************************\n");
	printf("|Print %% and a char after with space between\n");
	printf("\\********************************************\n""\x1b[32m");
	fflush(stdout);
	ft_printf("% a");
	//printf("% ");
	printf("<-----Should not work(flag with no specifier)");
	printf("\x1b[36m""\n#############################################\n\n");

	
	return(0);
}